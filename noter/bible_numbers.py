def get_book_ids():
    book_ids = {}
    lines = [_ for _ in stuff.split("\n") if _.strip()]

    identifier = 0
    for i, book in enumerate(lines):
        book_row = i % 2 == 0
        if not book_row:
            continue
        identifier += 1
        book_ids[identifier] = book
    return book_ids
